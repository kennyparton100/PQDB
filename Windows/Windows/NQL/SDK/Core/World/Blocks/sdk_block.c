{
    switch (type) {
        case BLOCK_WATER:
            return SDK_MATERIAL_FLUID_WATER;
        case BLOCK_LAVA:
            return SDK_MATERIAL_FLUID_LAVA;
        case BLOCK_SAND:
        case BLOCK_GRAVEL:
        case BLOCK_COARSE_ALLUVIUM:
        case BLOCK_TALUS:
        case BLOCK_BEACH_GRAVEL:
        case BLOCK_MARINE_SAND:
            return SDK_MATERIAL_GRANULAR_LOOSE;
        case BLOCK_COLLUVIUM:
        case BLOCK_CRUSHED_STONE:
        case BLOCK_SANDBAGS:
            return SDK_MATERIAL_GRANULAR_COMPACTED;
        case BLOCK_TOPSOIL:
        case BLOCK_SUBSOIL:
        case BLOCK_CLAY:
        case BLOCK_FINE_ALLUVIUM:
        case BLOCK_MARINE_MUD:
        case BLOCK_TIDAL_SILT:
        case BLOCK_WETLAND_SOD:
        case BLOCK_PEAT:
        case BLOCK_BERRY_BUSH:
        case BLOCK_REEDS:
        case BLOCK_TALL_GRASS:
        case BLOCK_DRY_GRASS:
        case BLOCK_FERN:
        case BLOCK_SHRUB:
        case BLOCK_HEATHER:
        case BLOCK_CATTAILS:
        case BLOCK_WILDFLOWERS:
        case BLOCK_CACTUS:
        case BLOCK_SUCCULENT:
        case BLOCK_MOSS:
            return SDK_MATERIAL_ORGANIC_SOFT;
        case BLOCK_SHALE:
        case BLOCK_MUDSTONE:
        case BLOCK_SILTSTONE:
        case BLOCK_MARL:
        case BLOCK_CHALK:
        case BLOCK_TUFF:
        case BLOCK_SCORIA:
        case BLOCK_SALT_EVAPORITE:
        case BLOCK_SAPROLITE:
            return SDK_MATERIAL_STATIC_WEAK;
        default:
            return SDK_MATERIAL_STATIC_COMPETENT;
    }
}

uint32_t sdk_block_get_behavior_flags(BlockType type)
{
    switch (sdk_block_get_runtime_material(type)) {
        case SDK_MATERIAL_FLUID_WATER:
        case SDK_MATERIAL_FLUID_LAVA:
            return SDK_BLOCK_BEHAVIOR_FLUID;
        case SDK_MATERIAL_GRANULAR_LOOSE:
            return SDK_BLOCK_BEHAVIOR_GRANULAR | SDK_BLOCK_BEHAVIOR_SUPPORT_SENSITIVE;
        case SDK_MATERIAL_GRANULAR_COMPACTED:
        case SDK_MATERIAL_STATIC_WEAK:
        case SDK_MATERIAL_ORGANIC_SOFT:
            return SDK_BLOCK_BEHAVIOR_SUPPORT_SENSITIVE;
        case SDK_MATERIAL_STATIC_COMPETENT:
        default:
            return 0u;
    }
}

uint8_t sdk_block_get_settling_rate(BlockType type)
{
    switch (sdk_block_get_runtime_material(type)) {
        case SDK_MATERIAL_FLUID_WATER:
            return 255u;
        case SDK_MATERIAL_FLUID_LAVA:
            return 48u;
        case SDK_MATERIAL_GRANULAR_LOOSE:
            return 180u;
        case SDK_MATERIAL_GRANULAR_COMPACTED:
            return 96u;
        default:
            return 0u;
    }
}

uint8_t sdk_block_get_angle_of_repose(BlockType type)
{
    switch (type) {
        case BLOCK_SAND:
        case BLOCK_MARINE_SAND:
            return 34u;
        case BLOCK_GRAVEL:
        case BLOCK_BEACH_GRAVEL:
        case BLOCK_COARSE_ALLUVIUM:
            return 38u;
        case BLOCK_TALUS:
        case BLOCK_CRUSHED_STONE:
            return 41u;
        case BLOCK_COLLUVIUM:
            return 36u;
        default:
            return 32u;
    }
}

