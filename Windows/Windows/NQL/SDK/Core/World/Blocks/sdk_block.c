static const SdkBlockDef* block_def_or_air(BlockType type)
{
    if ((int)type < 0 || type >= BLOCK_COUNT) {
        return &g_block_defs[BLOCK_AIR];
    }
    if (g_block_defs[type].id != type) {
        for (int i = 0; i < BLOCK_COUNT; ++i) {
            if (g_block_defs[i].id == type) {
                return &g_block_defs[i];
            }
        }
        return &g_block_defs[BLOCK_AIR];
    }
    return &g_block_defs[type];
}

const SdkBlockDef* sdk_block_get_def(BlockType type)
{
    return block_def_or_air(type);
}

const char* sdk_block_get_name(BlockType type)
{
    return block_def_or_air(type)->name;
}

const char* sdk_block_get_texture_asset(BlockType type, int face)
{
    if (face < 0 || face >= FACE_COUNT) face = 0;

    switch (type) {
        case BLOCK_GRASS:
            if (face == 3) return "grass_top";
            if (face == 2) return "dirt";
            return "grass_side";
        case BLOCK_TURF:
            if (face == 3) return "turf_top";
            if (face == 2) return "dirt";
            return "turf_side";
        case BLOCK_WETLAND_SOD:
            if (face == 3) return "wetland_sod_top";
            if (face == 2) return "dirt";
            return "wetland_sod_side";
        case BLOCK_LOG:
            if (face == 3 || face == 2) return "log_top";
            return "log_side";
        case BLOCK_CRAFTING_TABLE:
            if (face == 3) return "crafting_table_top";
            if (face == 2) return "planks";
            return "crafting_table_side";
        case BLOCK_FURNACE:
            if (face == 3) return "furnace_top";
            if (face == 2) return "furnace_bottom";
            return "furnace_side";
        case BLOCK_CAMPFIRE:
            if (face == 3) return "campfire_top";
            if (face == 2) return "campfire_bottom";
            return "campfire_side";
        case BLOCK_ANVIL:
            if (face == 3) return "anvil_top";
            if (face == 2) return "anvil_bottom";
            return "anvil_side";
        case BLOCK_BLACKSMITHING_TABLE:
            if (face == 3) return "blacksmithing_table_top";
            if (face == 2) return "blacksmithing_table_bottom";
            return "blacksmithing_table_side";
        case BLOCK_LEATHERWORKING_TABLE:
            if (face == 3) return "leatherworking_table_top";
            if (face == 2) return "leatherworking_table_bottom";
            return "leatherworking_table_side";
        default:
            return block_def_or_air(type)->name;
    }
}

